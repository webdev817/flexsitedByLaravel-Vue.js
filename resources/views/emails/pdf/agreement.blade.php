      <br>
      Quality Consortium Services, a Consortium and Third Party Administrator (C/TPA) which owns and
      manages a drug and alcohol testing program designed for businesses and regulated entities with safety
      sensitive and drug free employees or members. This agreement is made between the parties with the
      following understanding: <br>
      <br>
      <b>1.</b> Company elects to participate in Quality Consortium Services (C/TPA) program that is compliant with
      the requirements of the 49 CFR, Parts 40, 382, (FMCSA), 219 (FRA), 655 (FTA), 199 (PHMSA), 14 & 120
      (FAA), 46 CFR Parts 4 & 16 (USCG), for all Companies regulated by the Department of Transportation.
      <br>
      <b>2.</b> Company elects to retain Quality Consortium Services to administer its drug and alcohol testing
      program for compliance.
      <br>
      <b>3.</b> Non-regulated companies do not need to follow the criteria laid forth by the Department of
      Transportation (DOT) and may elect to enforce their own regulations per their Company Drug and
      Alcohol policy. The C/TPA will administer and manage their program separately.
      <br>
      <br>
      In consideration of the mutual obligations hereunder, both parties agree as follows:
      <br> <br>
      <strong>PART - 1</strong> <br>
      <i>Quality Consortium Services General Obligations and Responsibilities:</i>
      <br> <br>
      To administer the Company’s drug and alcohol testing program, Quality Consortium Services shall:
      <br>
      <b>1.</b> Maintain qualified random testing pools for regulated Company employees or members. Meet or
      exceed minimum testing rates as set forth by the Department of Transportation for the specific
      governing agency of the Company and/or other percentages defined by the Company for other
      employees or members. <br>
      <b>2.</b> Manage a random selection and notification program minimally consistent with DOT or other
      regulated controlled substance and alcohol testing requirements. <br>
      <b>3.</b> Provide the following: <br>
      &nbsp; &nbsp; • Access to Quality Consortium Services network of collection sites through Alere and Quest.
      <br>
      &nbsp; &nbsp; • A laboratory certified by the Department of Health and Human Services’ SAMHSA Agency.
      <br>
      &nbsp; &nbsp; • A Medical Review Officer (MRO) to provide test result notifications as required by DOT
      regulations. The MRO service is also provided for all non-DOT testing as an option.
      <br>
      4. Maintain records documenting Company’s participation in Quality Consortium Services random
      testing program and will provide Company with these records and other compliance information within
      two (2) business days of its designated Representatives (DER) request.
      <br>
      5. Upon Company’s request by written consent, Quality Consortium Services will release drug and
      alcohol (D&A) testing history and information in accordance with DOT, other regulatory agencies or a
      Company’s D&A Policy. Information requested may include: verification of participation in the C/TPA
      program and/or current enrollment status, test results if maintained by C/TPA for a Company’s driver or
      employee / member within the last two years, and positive information for the last 5 years (DOT) or
      consistent with other noted regulatory requirements.
    </td>
  </tr>
  <tr>
    <td width="100%">
      <strong>PART - 2</strong>
      <br>
      <i>Company’s Obligations and Responsibilities:</i>
      <br>
      <br>
      To qualify for and maintain enrollment in Quality Consortium Services C/TPA program, the Company
      agrees to: <br>
      <b>1.</b> Maintain a Company Policy for controlled substance and alcohol (D&A) misuse implemented before
      initiating testing. Complete verification of prior drug, alcohol violations for each new member. <br>
      <b>2.</b> For reasons of reasonable suspicion testing, Company agrees to have a person who supervises
      employees or members trained in the aid of recognizing the signs and symptoms of substance abuse and
      alcohol misuse, unless self employed. <br>
      <strong>3.</strong> Notify Quality Consortium Services immediately of any changes in Company information including
      name, address, telephone number(s), (office, mobile and/or emergency numbers), fax number(s),
      authorized contact(s), Designated Employer Representative (DER) or enrolled Company employee(s)
      information. <br>
      <strong>4.</strong> Notify Quality Consortium Services immediately of any changes in the status of a Company’s
      employee(s) or member(s) including termination, out-of-service, driver license changes, etc. (The
      Company understands that if there is more than a 30-day lapse of enrollment in a random testing
      program, they are responsible to have that employee / member submit to a pre-employment drug test
      before returning to a safety sensitive position.) <br>
      <b>5.</b> Have new employee / member complete a pre-employment drug screen, for DOT employees /
      members in safety sensitive positions. Employee / Member can only be added to the Random Pool with
      a negative DOT drug test result. If Company elects not to forward test results to C/TPA, they are
      responsible for maintaining and producing such records in the event of an agency audit. It is the
      Companies responsibility to ensure that employees / members are properly and adequately tested. <br>
      <b>6.</b> Have employee or member proceed immediately to the closest available authorized drug and/or
      alcohol collection site once they are notified of a random testing selection by the DER. <br>
      <b>7.</b> Fully document any reason(s) leading to the failure to comply with all requests for a random test.
      Failure to randomly test your employees or members may result in termination from Quality Consortium
      Services program and Company agrees to the release of this information to any applicable and
      appropriate regulatory enforcement agencies. <br>
      <b>8.</b> Determine if post-accident drug and alcohol testing is necessary following any accident. If testing is
      required, it is the Companies responsibility to locate a collection site and ensure the individual is tested
      within the appropriate timeframe. <br>
      <b>9.</b> Ensure an employee / member does not resume safety sensitive duties prior to SAP approval and
      Return-to-Duty testing. <br>
      <b>10.</b> Notify Quality Consortium Services immediately and remove employee / member from service upon
      notification from either: <br>
      &nbsp;&nbsp; a. A Medical Review Officer (MRO) that an individual tested positive on a DOT drug test; or <br>
      &nbsp;&nbsp; b. Breath Alcohol Technician (BAT) or Saliva Alcohol Technician that the individuals confirmation
      test result has an alcohol concentration of .02 or greater. <br>
      <b>11.</b> Responsibility to establish an alternate provision within their Company’s D&A policy pertaining to
      post accident testing when a Quality Consortium Services collection site or personnel is unavailable. (e.g.
      out of the area, on site needed, after hours, holidays, etc.). This cost is not covered under any
      provisions and is an additional and separate fee. <br>

      12. Have available a same gender observer, if necessary, when a direct observed collection is required
      and the collection site is unable to provide a same gender observer.
      <br>

      <b>13.</b> Should Company request the assistance of Quality Consortium Services to prepare the Management
        Information System Report (MIS), Company shall contact Quality Consortium Services at least three (3)
          business days prior to a scheduled enforcement inspection date. <br>
      <b>14.</b> Company accepts full responsibility for understanding the obligations under and participating in
        Quality Consortium Services C/TPA program in a manner that is compliant with the 49 CFR, Parts 40,
        382, (FMCSA), 219 (FRA), 655 (FTA), 199 (PHMSA), 14 & 120 (FAA), 46 CFR Parts 4 & 16 (USCG)
    </td>
