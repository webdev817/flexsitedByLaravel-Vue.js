      <strong>PART - 3</strong> <br>
<i>Fees for Services:</i> <br> <br>
<b>1.</b> For performance of Quality Consortium Services obligations listed in Part-1 of this Agreement,
Company shall pay a fee according to the current pricing schedule or prior written agreement at the
time of enrollment or renewal. <br>
<b>2. </b>Company is responsible to pay Quality Consortium Services directly for testing fees regardless of the
payment arrangements made between the Company and / or employee. <br>
<b>3.</b> Arrangement for split specimen testing fees shall be made between Company, employee/ member
and MRO at the time of the request; the cost of split specimen testing is not Quality Consortium Services
responsibility. Any split specimen testing will be automatically billed to the Company account. <br>
<b>4.</b> Company is responsible for payment directly to collector/collection facility for any fees if not using a
collection site designated by Quality Consortium Services. <br>
<b>5.</b> Should Company utilize entities for testing that are not affiliated with Quality Consortium Services,
e.g., using an outside collection site, it is the Company’s obligation to pay the cost of all fees associated
with the collection, testing and reporting directly to that site. <br>
<b>6.</b> All NSF check returns will be subject to an $80 handling fee. Any disputed online fee payments will
result in immediate removal from the Consortium and a freeze on the account, no requests will be
processed until account has been paid along with any dispute charges and fees. <br>
<b>7.</b> Company agrees to promptly pay all invoices or will be subject to a credit hold and penalty late fee to
accrue per month starting 30 days after Quality Consortium Services billing date. Company will be
expelled from Consortium after 60-days of non-payment and Company’s information will be sent to a
collections agency for payment at which time the Company is responsible for all collections fees applied
by the collection agency and legal fees incurred. Companies account will be frozen 4 days after the
initial payment was due. <br>
<b>8.</b> Credit Card Charges by Internet & Phone:
Payment on-line and by phone with credit card is preferred, and the client agrees not to dispute any
non-fraudulent charges once Quality Consortium Services products or services have been received. If
payment is disputed without cause, the client will be dropped permanently from our program, their
account data will be frozen and they will be sent to collections if the dispute is not resolved to the
satisfaction of both parties. <br>
<b>9.</b> Annual membership fee is due before account is active. Company may opt to enroll online instead of
sending in this contract. However they are still bound to the terms of this contract as agreed upon when
enrolling online. Membership will automatically renew 2 weeks prior to expiration unless canceled by
Company in writing. Membership renewals are automatically processed and billed to the credit card on
file for the Company. If timely payment is not received, the Company’s membership is revoked and their
data is frozen. <br>
<b>10.</b> Listed fees are subject to change. Written notice will be given for any rate changes. Changes will
take effect upon renewal of Service Agreement. <br>

<b>11.</b> <strong> Fee Schedule:</strong>
<br>
Annual Membership Fee - $49 Additional Employees / Members - $15
Drug test fees for in network collection sites through Alere or Quest, when ordered through C/TPA, are
as follows: <br>
&nbsp;&nbsp; DOT Drug Test - $59, includes Lab, MRO & Collection Fees. Observed testing adds $46 <br>
&nbsp; NON DOT 5 Panel - $59 includes Lab, MRO & Collection Fees. <br>
&nbsp; Breath Alcohol Screening - $49 <br>
&nbsp; Return-to-Duty / Observed testing - $59 PLUS additional fee imposed by collection site. <br>
&nbsp; Onsite testing - $59 for Lab, CCF and MRO fees PLUS additional fees imposed by collection site.  <br>
&nbsp; Supervisor Training - $35 /person, online course. <br>
<b>12.</b> Company is responsible for any and all fees incurred when using any service other than those
performed by Quality Consortium Services or its affiliated Quest and Alere collection sites. <br>
<b>13.</b> Unless otherwise indicated, C/TPA will automatically initiate the subscription fee for new Company
employees / members upon receipt of a negative pre-employment drug test, until the maximum
number of covered Company employees / members is reached. If a new employee / member is to
replace and old employee / member, the new member will take over the remaining subscription. <br>
<b>14.</b> Companies that have contracted with C/TPA as a sub-consortium will not have a limit on the
maximum number of enrolled members and must pay the listed subscription fee for each member. <br>




